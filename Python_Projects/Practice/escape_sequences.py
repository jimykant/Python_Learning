"""
\n - insert new/next line
\t - insert tab
\\ - insert backslash-\
\' - insert a single quote inside a single-quoted string
\"...\" - insert a double quote inside a double-quoted string
"""

# \n
print("Hello everyone.\nHow are you?")

# \t
print("John 20")
print("John\t20")

# \\
print("Yes\\No")

# \'
print('This is Python\'s class.')
print("This is Python's class.")

# \"
print("He says, \"we are learning python\"")
print('He says, "we are learning pyhton"')
