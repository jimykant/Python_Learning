""" 
Membership Operations
in - Check if the String/Character is the part of another String/Character or not
not in - Check if the String/Character is NOT the part of another String/Character or not
"""
print("# Membership Operators")
# in
print("1. in Operation")
s1 = "Python is fun"
print("Python" in s1)
print("i" in s1)
print("z" in s1)
print("java" in s1)

# not in
print("2. not in Operation")
print("Python" not in s1)
print("i" not in s1)
print("z" not in s1)
print("java" not in s1)
print("\n")

# Comparison of Strings
print("# Comparison of Strings")
print("Python" == "Python")
print("Python" != "Python")
